// 1. Crear un algoritmo que muestre los números impares entre el 0 y el 100.

// El bucle for se ejecuta 100 veces, incrementando el contador i en 2 por cada iteración. Al comenzar el bucle con i = 1,
//  logramos imprimir los números impares entre 0 y 100.
for (let i = 1; i <= 100; i += 2) {
    console.log(i)
}